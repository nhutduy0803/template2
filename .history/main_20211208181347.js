function openmenu() {
  var openmenu = document.querySelector(".sub-menu");
  openmenu.classList.add("openmenu");
}

function closemenu() {
  var openmenu = document.querySelector(".sub-menu");
  openmenu.classList.remove("openmenu");
}

function subhome() {
  var opensubhome = document.querySelector(".sub-home");
  opensubhome.classList.add("opensubhome");
}
